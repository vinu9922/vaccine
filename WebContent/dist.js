function setDist() {
     distsel = document.getElementById('state'); 
     distlist = dist[distsel.value];
     changeSelect('dist',distlist,distlist);
     setTaluka();
}

function setTaluka() {
     distsel = document.getElementById('state'); 
     talukasel = document.getElementById('dist');
     talukalist = taluka[distsel.value][talukasel.value];
     changeSelect('taluka',talukalist,talukalist);
}


function changeSelect(fieldId, newOptions, newValues) {

      selectField = document.getElementById( fieldId);
      selectField.options.length = 0;
      selectField.options[selectField.length] = new Option('Please select correct option',-1);
      for(i=1;i<newOptions.length ; i++){
            selectField.options[selectField.length] = new Option(newOptions[i],newValues[i]);
      }
}

function addLoadEvent(func) {
      var oldonload = window.onload;
      if(typeof window.onload != 'function'){
            window.onload = func;
      }else{
            window.onload = function(){
                  if(oldonload){
                        oldonload();
                  }
                  func();
            }
      }

}

addLoadEvent(function(){
      setDist();
});
























